const mongoose = require('mongoose');

const goods_accepted = new mongoose.Schema(
  {
    name: {
      type: String,
      required: true,
      unique: true,
      trim: true,
    },
    description: {
      type: String,
    },
    status: {
      type: String,
      enum: ['active', 'inactive'],
      default: 'active',
    },
  },
  {
    timestamps: true,
    collection: "goods_accepted",
  }
);

module.exports = mongoose.model('goods_accepted', goods_accepted); 